package eu.europeana.normalization.languages;

/**
 * List of standard language vocabularies that the Languages NAL allow to normalize into
 *
 * @author Nuno Freire (nfreire@gmail.com)
 */
public enum LanguagesVocabulary {

  /** ISO 639-1 codes. **/
  ISO_639_1,

  /** ISO 639-2b codes. **/
  ISO_639_2B,

  /** ISO 639-2t codes. **/
  ISO_639_2T,

  /** ISO 639-3 codes. **/
  ISO_639_3,

  /** EU language name authority list. See https://open-data.europa.eu/en/data/dataset/language. **/
  LANGUAGES_NAL
}
